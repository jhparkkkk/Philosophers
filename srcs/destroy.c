#include "philo.h"

void    destroy_philo(t_philo **ph)
{
    int i;

    i = 0;
    while (ph[i])
    {
        // if (ph[i]->p)
            // free(ph[i]->p);
        free(ph[i]);
        i++;
    }
    free(ph);
}
